function drawLevel() {
  // Loop through current level tiles and render objects to grid
  // Dynamically changes size of grids depending on number of grids in level to fill the canvas
  for (let i = 0; i < currentLevel.length; i++) {
    for (let j = 0; j < currentLevel[i].length; j++) {
      // Get the sprite for the object
      let tile = currentLevel[i][j];
      if (typeof tile == "object") {
        if (setup) {
          tile.setCoordinates(i*tileSize,j*tileSize);
        }
        if ((tile.state.name != "door" || (tile.state.name == "door" && !tile.isOpen())) && inVision(tile.state.x, tile.state.y)) {
          ctx.drawImage(tile.state.sprite,tile.state.x,tile.state.y);
        }
      } else if (tile == 1 && inVision(i * tileSize, j * tileSize)) {
          ctx.fillStyle = "#2e2e2e";
          ctx.fillRect(i * tileSize, j * tileSize, tileSize, tileSize);
        }
      }
    }
  }

function inVision(x,y) {
  let vision = false;
  visionItems.forEach(visionItem => {
    if (Math.hypot(x-visionItem.x, y-visionItem.y) <= maxVision) {
      vision = true;
    }
  });
  return vision;
}


let clicked = 0;
let curPos;

canvas.addEventListener("mousemove", click, false);
function click(event) {
    let x = Math.floor((event.pageX - canvas.offsetLeft) / tileSize);
    let y = Math.floor((event.pageY - canvas.offsetTop) / tileSize);
    if (clicked == 1 && curPos != x + " " + y) {
      currentLevel[x][y] = (currentLevel[x][y] === 1 ? 0 : 1);
    }
    curPos = x + " " + y;
};

canvas.addEventListener("mousedown", down, false);
function down() {
    clicked = 1;
    let x = Math.floor((event.pageX - canvas.offsetLeft) / tileSize);
    let y = Math.floor((event.pageY - canvas.offsetTop) / tileSize);
    currentLevel[x][y] = (currentLevel[x][y] === 1 ? 0 : 1);
}

canvas.addEventListener("mouseup", up, false);
function up() {
    clicked = 0;
}

